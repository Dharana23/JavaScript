// Number to String
let val = 5;
val = String(5);

// Boolean to String
val = String(true);

// Date to String
val = String(new Date());

// Array to String
val = String([1, 2, 3, 4]);

// toString() method
val = (5).toString();

// String to Number
val = Number('5');

// parseInt & parseFloat
val = parseInt('100.30');
val = parseFloat('100.31');

console.log(val);